int utf8_char_len(unsigned char c)
{
    if (c < 0x80)
        return 1;
    if ((c & 0xE0) == 0xC0)
        return 2;
    if ((c & 0xF0) == 0xE0)
        return 3;
    if ((c & 0xF8) == 0xF0)
        return 4;
    return 1;
}

string scale_line_x2(const string &line)
{
    string result;
    int i = 0;
    while (i < (int)line.size())
    {
        int len = utf8_char_len((unsigned char)line[i]);
        string codepoint = line.substr(i, len);
        result += codepoint;
        result += codepoint;
        i += len;
    }
    return result;
}

vector<string> scale2x(const vector<string> &lines)
{
    vector<string> result;
    for (const string &line : lines)
    {
        string scaled = scale_line_x2(line);
        result.push_back(scaled);
    }
    return result;
}

